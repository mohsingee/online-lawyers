    // Language dropdown interactions
    (function () {
      var dropdowns = document.querySelectorAll('.locale-class');
      function closeAll(except) {
        document.querySelectorAll('.lang-dropdown').forEach(function (menu) {
          if (!except || menu !== except) menu.classList.remove('show');
        });
      }
      dropdowns.forEach(function (wrapper) {
        var trigger = wrapper.querySelector('.current-lang');
        var menu = wrapper.querySelector('.lang-dropdown');
        if (!trigger || !menu) return;
        trigger.addEventListener('click', function (e) {
          e.preventDefault();
          e.stopPropagation();
          var isOpen = menu.classList.contains('show');
          closeAll();
          if (!isOpen) menu.classList.add('show');
        });
        menu.addEventListener('click', function (e) { e.stopPropagation(); });
      });
      document.addEventListener('click', function () { closeAll(); });
      document.addEventListener('keydown', function (e) { if (e.key === 'Escape') closeAll(); });
      // Update language selection text and icon when chosen
      document.querySelectorAll('.lang-option').forEach(function (item) {
        item.addEventListener('click', function (e) {
          e.preventDefault();
          var lang = this.getAttribute('data-lang');
          localStorage.setItem('site_lang', lang);
          var label = lang === 'ar' ? 'Arabic' : 'English';
          document.querySelectorAll('.lang-text').forEach(function (t) { t.textContent = label; });
          document.querySelectorAll('.locale-class .lang-flag').forEach(function (img) {
            if (lang === 'ar') {
              img.setAttribute('src', 'https://cdn.jsdelivr.net/gh/lipis/flag-icons/flags/4x3/ae.svg');
            } else {
              img.setAttribute('src', 'https://cdn.jsdelivr.net/gh/lipis/flag-icons/flags/4x3/gb.svg');
            }
          });
          // Close menus after selection
          closeAll();
        });
      });
      // Initialize from localStorage
      var saved = localStorage.getItem('site_lang');
      if (!saved) {
        saved = 'en';
        localStorage.setItem('site_lang', saved);
      }
      var label = saved === 'ar' ? 'Arabic' : 'English';
      document.querySelectorAll('.lang-text').forEach(function (t) { t.textContent = label; });
